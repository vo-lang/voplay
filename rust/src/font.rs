//! Built-in bitmap font for text rendering.
//!
//! Provides a simple 8x8 ASCII bitmap font (code points 32–126).
//! The font is embedded as a compile-time constant and rasterized into a
//! texture atlas on first use. Text is rendered using the sprite pipeline.

use crate::pipeline_sprite::{SpriteDraw, SpriteInstance};
use crate::texture::TextureId;

/// 8×8 bitmap font covering ASCII 32–126 (95 printable characters).
/// Each character is 8 bytes (one byte per row, MSB = leftmost pixel).
/// Font data derived from classic PC BIOS font.
const GLYPH_W: u32 = 8;
const GLYPH_H: u32 = 8;
const FIRST_CHAR: u8 = 32;
const LAST_CHAR: u8 = 126;
const NUM_GLYPHS: u32 = (LAST_CHAR - FIRST_CHAR + 1) as u32; // 95
const ATLAS_COLS: u32 = 16;
const ATLAS_ROWS: u32 = (NUM_GLYPHS + ATLAS_COLS - 1) / ATLAS_COLS; // 6

/// The built-in font state.
pub struct BuiltinFont {
    pub texture_id: TextureId,
    pub atlas_w: u32,
    pub atlas_h: u32,
}

impl BuiltinFont {
    /// Create the font atlas texture. Must be called after renderer init.
    pub fn create(
        texture_manager: &mut crate::texture::TextureManager,
        device: &wgpu::Device,
        queue: &wgpu::Queue,
    ) -> Self {
        let atlas_w = ATLAS_COLS * GLYPH_W;
        let atlas_h = ATLAS_ROWS * GLYPH_H;
        let mut rgba = vec![0u8; (atlas_w * atlas_h * 4) as usize];

        for i in 0..NUM_GLYPHS {
            let glyph = &FONT_DATA[i as usize];
            let col = i % ATLAS_COLS;
            let row = i / ATLAS_COLS;
            let base_x = col * GLYPH_W;
            let base_y = row * GLYPH_H;

            for gy in 0..GLYPH_H {
                let bits = glyph[gy as usize];
                for gx in 0..GLYPH_W {
                    let on = (bits >> (7 - gx)) & 1;
                    let px = base_x + gx;
                    let py = base_y + gy;
                    let idx = ((py * atlas_w + px) * 4) as usize;
                    if on == 1 {
                        rgba[idx] = 255;     // R
                        rgba[idx + 1] = 255; // G
                        rgba[idx + 2] = 255; // B
                        rgba[idx + 3] = 255; // A
                    }
                    // else: stays (0,0,0,0) = transparent
                }
            }
        }

        let texture_id = texture_manager.load_rgba(device, queue, atlas_w, atlas_h, &rgba);

        Self {
            texture_id,
            atlas_w,
            atlas_h,
        }
    }

    /// Generate sprite instances for a text string.
    /// Returns a Vec of SpriteDraw for each visible character.
    pub fn layout_text(
        &self,
        text: &str,
        x: f32,
        y: f32,
        size: f32,
        r: f32,
        g: f32,
        b: f32,
        a: f32,
    ) -> Vec<SpriteDraw> {
        let scale = size / GLYPH_H as f32;
        let char_w = GLYPH_W as f32 * scale;
        let char_h = GLYPH_H as f32 * scale;
        let atlas_w_f = self.atlas_w as f32;
        let atlas_h_f = self.atlas_h as f32;
        let glyph_u = GLYPH_W as f32 / atlas_w_f;
        let glyph_v = GLYPH_H as f32 / atlas_h_f;

        let mut draws = Vec::with_capacity(text.len());
        let mut cx = x;

        for ch in text.bytes() {
            if ch < FIRST_CHAR || ch > LAST_CHAR {
                cx += char_w; // advance even for unknown chars
                continue;
            }
            let idx = (ch - FIRST_CHAR) as u32;
            let col = idx % ATLAS_COLS;
            let row = idx / ATLAS_COLS;
            let u0 = col as f32 * glyph_u;
            let v0 = row as f32 * glyph_v;
            let u1 = u0 + glyph_u;
            let v1 = v0 + glyph_v;

            draws.push(SpriteDraw {
                texture_id: self.texture_id,
                instance: SpriteInstance {
                    dst_rect: [cx, y, char_w, char_h],
                    src_rect: [u0, v0, u1, v1],
                    color: [r, g, b, a],
                    params: [0.0, 0.0, 0.0, 0.0], // no rotation/flip
                },
            });

            cx += char_w;
        }

        draws
    }
}

